#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	int i;

	if (s)
	{
		i = 0;
		while (s[i] != '\0')
		{
			ft_putchar_fd((char)s[i], fd);
			i++;
		}
	}
}
